#include "7Segment.h"

void segmentControl::switch_seg_States() const
{
  if (this->Fields.flags == 0x1)
  {
    this->lightNumber(time_now->time_sec_value1);
  }

  else if (this->Fields.flags == 0x2)
  {
    this->lightNumber(time_now->time_sec_value2);
  }

} ///

void segmentControl::timingOps()
{
  this->clear_segments();

  this->segFastSwitch();
  this->switch_seg_States();

  this->switch_seg++;

  if (this->switch_seg > 1)
    this->switch_seg = 0;
}

void segmentControl::clear_segments() const
{
  digitalWrite(segA, LOW);
  digitalWrite(segB, LOW);
  digitalWrite(segC, LOW);
  digitalWrite(segD, LOW);
  digitalWrite(segE, LOW);
  digitalWrite(segF, LOW);
  digitalWrite(segG, LOW);
}

void segmentControl::segFastSwitch()
{
  switch (this->switch_seg)
  {

    case 0:
      this->Fields.flags = 0x1;
      break;

    case 1:
      this->Fields.flags = 0x2;
      break;

    default:
      break;
  }
}

